function Auth_come() {
    let bg = document.querySelector(".auth_page_bg");
    let auth = document.querySelector(".auth ");
    bg.style.display = "";
    auth.style.display = "";
}

function Auth_left() {
    let bg = document.querySelector(".auth_page_bg");
    let auth = document.querySelector(".auth ");
    bg.style.display = "none";
    auth.style.display = "none";
}